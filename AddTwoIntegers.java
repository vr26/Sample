public class AddTwoIntegers 
{

    public static void main(String[] args) 
    {
        
        /* Declaring 2 Integer values */
        int first = 10;
        int second = 20;

        System.out.println("Enter two numbers: " + first + " " + second);
        
        /* Addition Process */
        int sum = first + second;
        /* Displaying */
        System.out.println("The sum is: " + sum);
    }
}